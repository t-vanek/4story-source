
CREATE PROCEDURE [dbo].[TTournamentStatus]
@wID SMALLINT,
@bGroup TINYINT,
@bStep TINYINT
AS

IF EXISTS(SELECT * FROM TTOURNAMENTSTATUSTABLE)
	UPDATE TTOURNAMENTSTATUSTABLE SET wID = @wID, bGroup = @bGroup, bStep = @bStep 
ELSE
	INSERT INTO TTOURNAMENTSTATUSTABLE (wID, bGroup, bStep) VALUES(@wID, @bGroup, @bStep)


