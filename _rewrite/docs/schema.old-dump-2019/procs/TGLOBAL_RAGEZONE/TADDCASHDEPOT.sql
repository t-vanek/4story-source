CREATE PROCEDURE [dbo].[TADDCASHDEPOT]
@dwUserID INT,
@dwTIME INT
AS

DECLARE @dwENDTIME SMALLDATETIME
DECLARE @dwENDTIME2 SMALLDATETIME
DECLARE @dwSTARTTIME SMALLDATETIME

SET @dwENDTIME = (DATEADD("d", @dwTIME, GETDATE()))

SET @dwSTARTTIME = (SELECT dCabinetUse FROM TUSERINFOTABLE WHERE dwUserID = @dwUserID)

BEGIN
if (@dwSTARTTIME <= GETDATE())
	UPDATE TUSERINFOTABLE SET dCabinetUse = @dwENDTIME WHERE dwUserID = @dwUserID

END

BEGIN
if (@dwSTARTTIME >= GETDATE())
	SET @dwENDTIME2 = (DATEADD("d", @dwTIME, @dwSTARTTIME))
	UPDATE TUSERINFOTABLE SET dCabinetUse = @dwENDTIME2 WHERE dwUserID = @dwUserID
END

