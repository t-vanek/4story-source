
CREATE PROCEDURE [dbo].[TGuildVolunteering] 
@bType TINYINT,
@dwCharID INT,
@dwID INT
AS

IF EXISTS(SELECT dwCharID FROM TGUILDVOLUNTEERTABLE WHERE dwCharID=@dwCharID AND bType=@bType)
	UPDATE TGUILDVOLUNTEERTABLE SET dwID=@dwID WHERE dwCharID=@dwCharID AND bType=@bType
ELSE
	INSERT INTO TGUILDVOLUNTEERTABLE (dwCharID, bType, dwID) VALUES(@dwCharID,@bType, @dwID)



