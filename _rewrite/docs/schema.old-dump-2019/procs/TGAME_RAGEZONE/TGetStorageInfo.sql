
CREATE PROCEDURE [dbo].[TGetStorageInfo]
@dwUserID INT,
@timeUse SMALLDATETIME OUTPUT,
@dwItemCount INT OUTPUT
AS

SET @timeUse = 0
SET @dwItemCount = 0

SELECT @timeUse = dCabinetUse FROM TGLOBAL_GSP.dbo.TUSERINFOTABLE WHERE dwUserID=@dwUserID
SELECT @dwItemCount = COUNT(dwID) FROM  TVIEW_CASHITEMCABINET INNER JOIN TITEMCHART ON TVIEW_CASHITEMCABINET.wItemID = TITEMCHART.wItemID WHERE dwUserID = @dwUserID AND bIsSpecial = 0


