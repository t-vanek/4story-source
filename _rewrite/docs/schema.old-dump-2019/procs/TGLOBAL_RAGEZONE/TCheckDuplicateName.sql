CREATE PROCEDURE [dbo].[TCheckDuplicateName]
@dwUserID INT,
@szNAME VARCHAR(50)
AS
	BEGIN TRAN CHECK_DUPLICATENAME
	DECLARE @dwReservedID INT

	IF EXISTS( SELECT TOP 1 dwCharID FROM TALLCHARTABLE WHERE szNAME = @szNAME)
	BEGIN
		COMMIT TRAN CHECK_DUPLICATENAME
		RETURN 1
	END
	IF EXISTS(SELECT TOP 1 * FROM TKEEPINGNAME WHERE @szName like(szName))
	BEGIN
		COMMIT TRAN CHECK_DUPLICATENAME
		RETURN 1
	END

	SELECT @dwReservedID = dwUserID FROM TRESERVEDNAME WHERE szNAME = @szNAME
	IF(@@ROWCOUNT <> 0 AND @dwReservedID IS NOT NULL)
	BEGIN
		IF(@dwReservedID <> @dwUserID)
		BEGIN
			COMMIT TRAN CHECK_DUPLICATENAME
			RETURN 1
		END
	END

	DELETE TRESERVEDNAME WHERE szNAME = @szNAME
	INSERT INTO TRESERVEDNAME VALUES(@dwUserID, @szNAME)

	COMMIT TRAN CHECK_DUPLICATENAME

